namespace Science
{
    /// <summary>
    /// Constants
    /// </summary>
    public class Constants
    {
        // Some pi numbers

        /// <summary>
        /// Rounded pi
        /// </summary>
        public const double pi = 3.14;
        /// <summary>
        /// Rounded two pi
        /// </summary>
        public const double dpi = 6.18;
